int main(int argc, char *argv[])
{
  char buf[BUF_SIZE], *newName;
  size_t bytesWritten, bytesRead, count = 0;
  int result = 0;
  FILE *out, *coff;
  MD5_CTX ctx;
  struct stat fstat;

  if (argc != 2) {
    printf("Usage: %s driver\n", argv[0]);
  }
  else {
    newName = (char *)malloc(strlen(argv[1]) + 7); /* append ".bless\0" */
    if (newName) {
      sprintf(newName, "%s.bless", argv[1]);
      out = fopen(newName,"wb");
      if (out) {
        coff = fopen(argv[1], "rb");
        if (coff) {
          result = stat(argv[1], &fstat);
          if (!result) {
            MD5Init(&ctx);
            /* now, load coff file and run md5 over it up until symbol table */
            bytesRead = fread(buf, 1, BUF_SIZE, coff);
            do {
              MD5Update(&ctx, buf, bytesRead);
              bytesRead = fread(buf, 1, BUF_SIZE, coff);
            } while(bytesRead > 0);
            MD5Final((unsigned char *)(bless_template + DRIVER_MD5_OFFSET), &ctx);
            /* write blessings template */
            bytesWritten = fwrite(bless_template, DRIVER_BLESS_TEMPLATE_LENGTH, 1, out);
          }
          else {
            printf("Cannot stat driver!\n");
            result = -4;
          }
          fclose(coff);
        }
        else {
          result = -3;
        }
        fclose(out);
      }
      else {
        result = -2;
      }
      free(newName);
    }
    else {
      result = -1;
    }
  }
  return result;
}
