int main(int argc, char *argv[])
{
  char buf[BUF_SIZE], *newName;
  unsigned long symbolTableOffset;
  size_t bytesWritten, bytesRead, count = 0;
  int result = 0, totalSize = 0, datSectors = 0, numHashes, i;
  FILE *out, *coff;
  MD5_CTX ctx;
  struct stat appStat, datStat;

  if (argc < 2) {
    printf("Usage: %s coff-file [app-type]\n", argv[0]);
  }
  else {
    newName = (char *)malloc(strlen(argv[1]) + 7); /* append ".bless\0" */
    if (newName) {
      sprintf(newName, "%s.bless", argv[1]);
      out = fopen(newName,"wb");
      if (out) {
        result = stat(argv[1], &appStat);
        if (!result) {
          totalSize += appStat.st_size;
          result = stat(DATA_FILE_NAME, &datStat);
          if (!result) {
            totalSize += datStat.st_size;
            datSectors = (datStat.st_size + (DVD_SECTOR_SIZE - 1)) / DVD_SECTOR_SIZE;
          }
          totalSize += (APP_BLESS_TEMPLATE_LENGTH + APP_SIG_TEMPLATE_LENGTH);
          numHashes = ((totalSize + (HASH_BLOCK_SIZE - 1)) / HASH_BLOCK_SIZE) * 16;
          totalSize += numHashes;
          totalSize += (HASH_BLOCK_SIZE - 1);
          numHashes = totalSize / HASH_BLOCK_SIZE;
          coff = fopen(argv[1], "rb");
          if (coff) {
            MD5Init(&ctx);
            /* now, load coff file and run md5 over it up until symbol table */
            bytesRead = fread(buf, 1, BUF_SIZE, coff);
            symbolTableOffset = *((unsigned long *)(buf + 8));
            symbolTableOffset = (symbolTableOffset & 0x000000FF) << 24 |
              (symbolTableOffset & 0x0000FF00) << 8 |
              (symbolTableOffset & 0x00FF0000) >> 8 |
              (symbolTableOffset & 0xFF000000) >> 24;
            while ((symbolTableOffset - count) > BUF_SIZE && (bytesRead == BUF_SIZE)) {
              MD5Update(&ctx, buf, BUF_SIZE);
              count += bytesRead;
              bytesRead = fread(buf, 1, BUF_SIZE, coff);
            }
            /* now symbolTableOffset - count bytes left over in buf */
            MD5Update(&ctx, buf, symbolTableOffset - count);
            MD5Final((unsigned char *)(bless_template + 32), &ctx);
            /* adjust blessings fields */
            *((unsigned long *)(bless_template + HASH_LIST_OFFSET)) = htonl(APP_BLESS_TEMPLATE_LENGTH);
            *((unsigned long *)(bless_template + HASH_LIST_LENGTH_OFFSET)) = htonl((numHashes * 16));
            *((unsigned long *)(bless_template + COFF_START_OFFSET)) =
              htonl(APP_BLESS_TEMPLATE_LENGTH + (numHashes * 16) + APP_SIG_TEMPLATE_LENGTH);
            if (datSectors > 0) {
              *((unsigned long *)(bless_template + DATA_FILE_LENGTH_OFFSET)) = htonl(datSectors);
            }
            /* write blessings template */
            bytesWritten = fwrite(bless_template, APP_BLESS_TEMPLATE_LENGTH, 1, out);
            for(i = 0; i < numHashes; i++) {
              bytesWritten = fwrite(hash_template, HASH_TEMPLATE_LENGTH, 1, out);
            }
            /* set application type if passed in */
            if (argc >= 3) {
              /* reuse 'i' */
              i = atoi(argv[2]);
              *(bless_template + NUON_APP_TYPE) = i;
            }
            bytesWritten = fwrite(sig_template, APP_SIG_TEMPLATE_LENGTH, 1, out);
            /* now fill in blank MD5 hashes */
            fclose(coff);
          }
          else {
            result = -3;
          }
        }
        fclose(out);
      }
      else {
        result = -2;
      }
      free(newName);
    }
    else {
      result = -1;
    }
  }
  return result;
}
